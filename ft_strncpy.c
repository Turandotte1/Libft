char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	int	s;

	s = 0;
	while (n > 0 && src[s])
	{
		dest[s] = src[s];
		s++;
		n--;
	}
	while (n > 0)
	{
		dest[s] = '\0';
		s++;
		n--;
	}
	return (dest);
}
