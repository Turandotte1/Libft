#include <libft.h>

void	ft_putnbr_fd(int n, int fd)
{
	long cnb;

	cnb = (long)nb;
	if (nb < 0)
	{
		ft_putchar_fd('-', fd);
		nb = nb * -1;
	}
	if (nb >= 10)
		ft_putnbr(nb / 10);
	ft_putchar_fd(nb % 10 + '0', fd);
}
